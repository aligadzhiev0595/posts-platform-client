import Image from 'next/future/image'
import type { NextPage } from 'next'
import Head from 'next/head'
import { Navbar } from '../../layouts/Navbar'
import { useRouter } from 'next/router'
import {
  BackBtn,
  Container,
  Img,
  PostDescr,
  PostItem,
  PostsWrapper,
  PostTitle,
  RemoveBtn,
  Wrapper,
} from '../../styles/components'
import { GetServerSideProps, InferGetServerSidePropsType } from 'next'
import axios from 'axios'

export const getServerSideProps: GetServerSideProps = async (context) => {
  const post = await (
    await axios(`https://posts-platform-backend.vercel.app/api/post/${context.query.id}`)
  ).data
  return {
    props: {
      post,
    },
  }
}

const Post: NextPage = ({
  post,
}: InferGetServerSidePropsType<typeof getServerSideProps>) => {
  const route = useRouter()
  const removePost = async () => {
    await axios
      .post('https://posts-platform-backend.vercel.app/api/post/remove', {
        postId: post._id,
      })
      .then(() => route.push('/'))
  }
  return (
    <>
      <Head>
        <title>{post.title}</title>
        <meta name='description' content='Generated by create next app' />
      </Head>
      <Navbar />
      <Wrapper>
        <Container>
          <BackBtn onClick={() => route.push('/')}>
            <Image
              src='/static/images/Vector.svg'
              alt='Picture of the author'
              width={24}
              height={15}
            />
            Назад
          </BackBtn>
          <PostsWrapper>
            <PostItem>
              <PostTitle>{post?.title}</PostTitle>
              <PostDescr>{post?.desc}</PostDescr>
            </PostItem>
            <PostItem>
              <Img src={post?.image} alt='Picture of the author' />
            </PostItem>
            <RemoveBtn onClick={removePost}>Удалить статью</RemoveBtn>
          </PostsWrapper>
        </Container>
      </Wrapper>
    </>
  )
}
export default Post
